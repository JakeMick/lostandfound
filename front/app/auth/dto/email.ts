
export class Email {
    email: string;
    
    constructor() {
        this.email = ''
    }

}

export class Credential {

    email: string;
    password: string;

    constructor() {
        this.email = '';
        this.password = '';
    }

}

export class User {

    username: string;
    password: string;
    email: string;
    tracker: string;
    

    constructor() {
        this.username = '';
        this.password = '';
        this.email = '';
        this.tracker = '';
    }

}